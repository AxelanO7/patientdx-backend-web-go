package delivery

import (
	"assyarif-backend-web-go/domain"
	"strconv"

	"github.com/asaskevich/govalidator"
	"github.com/gofiber/fiber/v2"
)

type PatientHandler struct {
	PatientUC domain.PatientUseCase
}

func NewPatientHandler(c *fiber.App, das domain.PatientUseCase) {
	handler := &PatientHandler{
		PatientUC: das,
	}
	api := c.Group("/stion")

	_ = api.Group("/public")

	private := api.Group("/private")
	private.Get("/patient", handler.GetAllPatient)
	private.Get("/patient/:id", handler.GetPatientByID)
	private.Post("/patient", handler.CreatePatient)
	private.Put("/patient/:id", handler.UpdatePatient)
	private.Delete("/patient/:id", handler.DeletePatient)
}

func (t *PatientHandler) GetAllPatient(c *fiber.Ctx) error {
	res, err := t.PatientUC.FetchPatients(c.Context())
	if err != nil {
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": err,
			"error":   err.Error(),
		})
	}
	return c.Status(fiber.StatusOK).JSON(fiber.Map{
		"status":  200,
		"success": true,
		"data":    res,
		"message": "Successfully get all patient",
	})
}

func (t *PatientHandler) GetPatientByID(c *fiber.Ctx) error {
	id := c.Params("id")
	strId, erStr := strconv.Atoi(id)
	if erStr != nil {
		return c.Status(fiber.StatusUnprocessableEntity).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": "Failed to parse id",
			"error":   erStr.Error(),
		})
	}
	res, err := t.PatientUC.FetchPatientByID(c.Context(), uint(strId))
	if err != nil {
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": err,
			"error":   err.Error(),
		})
	}
	return c.Status(fiber.StatusOK).JSON(fiber.Map{
		"status":  200,
		"success": true,
		"data":    res,
		"message": "Successfully get patient by id",
	})
}

func (t *PatientHandler) CreatePatient(c *fiber.Ctx) error {
	req := new(domain.Patient)
	if err := c.BodyParser(req); err != nil {
		return c.Status(fiber.StatusUnprocessableEntity).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": "Failed to parse body",
			"error":   err,
		})
	}
	valRes, er := govalidator.ValidateStruct(req)
	if !valRes {
		return c.Status(fiber.StatusUnprocessableEntity).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": "Failed to parse body",
			"error":   er.Error(),
		})
	}
	res, err := t.PatientUC.CreatePatient(c.Context(), req)
	if err != nil {
		return c.Status(fiber.StatusInternalServerError).JSON(fiber.Map{
			"status":  500,
			"success": false,
			"message": err,
			"error":   err.Error(),
		})
	}
	return c.Status(fiber.StatusCreated).JSON(fiber.Map{
		"status":  201,
		"success": true,
		"data":    res,
		"message": "Successfully create patient",
	})
}

